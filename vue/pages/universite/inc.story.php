        <h2>L’histoire de Hudson Bay : La vision de Samuel Hawthorne</h2>
        <p>
            L’Université de Hudson Bay a été fondée en 1978 par Samuel Hawthorne, un visionnaire et entrepreneur social originaire de New York.
            Ancien professeur et passionné par l’éducation, Hawthorne a toujours cru que l’enseignement supérieur ne devait pas simplement transmettre des connaissances, mais aussi former des citoyens du monde capables de relever les défis globaux du futur.
        </p>
        <br>

        <p>Dans les années 1970, New York était un carrefour mondial de culture, de politique et d’économie, mais aussi un lieu marqué par des inégalités sociales et une fracture croissante entre les différentes classes sociales.
            Samuel Hawthorne voyait dans cette situation un besoin urgent de changement, et il savait que l’éducation pouvait en être la clé.
        </p>
        <br>

        <div id="text_story">

            <h3><i class="fa-solid fa-star"></i>La Fondation de l’Université : 1978</h3>
            <p>En 1978, après une intense période de préparation et de levée de fonds, Samuel Hawthorne lança officiellement l’Université de Hudson Bay.
                Il choisit ce nom en hommage à la baie de Hudson, symbole de liberté, de découvertes et de vastitude.
                L’université, comme la baie elle-même, devait être un point de rencontre où des horizons différents se rejoindraient, où les idées circuleraient librement et où la créativité et l’innovation seraient au cœur des formations proposées.

            </p>

            <h3><i class="fa-solid fa-star"></i>Le but de Samuel Hawthorne</h3>

            <p>
                Pour lui, l’Université de Hudson Bay serait avant tout un lieu où les étudiants apprendraient à devenir des leaders, non seulement dans leurs domaines de spécialité, mais aussi dans leur manière d’aborder les enjeux sociaux, politiques et environnementaux.
                Sa vision était de former des citoyens engagés, capables de naviguer dans un monde de plus en plus globalisé, tout en restant ancrés dans des valeurs humaines et universelles.

            </p>

            <h3><i class="fa-solid fa-star"></i>Une Université différente</h3>
            <p>
                Samuel Hawthorne introduit des programmes interdisciplinaires qui favorisent la collaboration entre différentes spécialités, encourageant ainsi une approche globale des problèmes du monde moderne.
                L’université met également un point d’honneur à intégrer les aspects pratiques à l’enseignement théorique, offrant à ses étudiants de nombreuses opportunités de stages et de projets réels dès le début de leurs études.
            </p>
            <p>
                L’une des premières actions de Samuel Hawthorne fut de créer des bourses pour les étudiants issus de milieux défavorisés, permettant à des jeunes brillants mais sans ressources de suivre une formation de haut niveau.
                À travers ces bourses, il aspirait à ouvrir des portes et à construire une communauté d’apprentissage qui soit véritablement inclusive.
            </p>

            <h3><i class="fa-solid fa-star"></i>Un héritage durable</h3>
            <p>
                Aujourd'hui, plus de quarante ans après sa fondation, l’Université de Hudson Bay est un établissement prestigieux, respecté dans le monde entier pour son excellence académique et son engagement social.
                Le rêve de Samuel Hawthorne d’offrir une éducation de qualité et accessible à tous s’est matérialisé dans une institution qui fait la fierté de ses étudiants, de ses diplômés et de toute la communauté new-yorkaise.
            </p>
        </div>
